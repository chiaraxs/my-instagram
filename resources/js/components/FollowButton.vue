<template>
    <div class="container">
        <div class="row justify-content-center">
            <div class="col-8">
                
               <!-- follow button  -->
                <button class="btn btn-info mx-3" href="#" role="button" @click="followUser" v-text="buttonText"></button>
                <!-- /follow button -->

            </div>
        </div>
    </div>
</template>

<script>
    export default {
        props: [
            'userId',
            'follows'
        ],

        mounted() {
            console.log('Component mounted.')
        },

        data: function(){
            return{
                status: this.follows,
            }
        },

        methods: {
            followUser(){
                axios.post('/follow/' + this.userId)
                    .then(response => {
                        this.status = ! this.status;

                        console.log(response.data);
                    })
                    // .catch(errors => {
                    //     if (errors.response.status == 401){
                    //         window.location = '/login';
                    //     }
                    // });
            }
        },

        computed: {
            buttonText(){
                return (this.status) ? 'Unfollow' : 'Follow';    // se segui giÃ  -> unfollow : altrimenti -> follow
            }
        }
    }
</script>
